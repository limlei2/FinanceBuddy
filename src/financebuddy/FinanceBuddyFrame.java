    private void jButtonRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRefreshActionPerformed

        Connection con = Connect.ConnectDB();

        DefaultTableModel aModel = (DefaultTableModel) jTable2.getModel();
        java.util.Date date = jDateChooserDate1.getDate();
        LocalDate localdate = date.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        Date sqlDate = Date.valueOf(localdate);
        String username = jLabelUsername.getText();

        Statement stmt;
        try{
            stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT itemname, category, price FROM item WHERE username LIKE '"+username+"' AND DATE LIKE '"+sqlDate+"'");
            java.sql.ResultSetMetaData rsmd = rs.getMetaData();
            aModel.setNumRows(0);
            while(rs.next()) {
                Object[] objects = new Object [rsmd.getColumnCount()];
                for(int i = 0; i < rsmd.getColumnCount(); i++) {
                    objects[i] = rs.getObject(i+1);
                }
                aModel.addRow(objects);
            }
            jTable2.setModel(aModel);
        } catch(SQLException e){
            System.out.print(e);
        }
    }//GEN-LAST:event_jButtonRefreshActionPerformed

