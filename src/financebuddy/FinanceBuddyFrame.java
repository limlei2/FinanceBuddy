    public int getMonth(){
        LocalDate localdate = LocalDate.now(); 
        java.util.Date d = Date.valueOf(localdate);
        DateFormat dateMonth = new SimpleDateFormat("MM");
        int month = Integer.parseInt(dateMonth.format(d));
        return month;
    }
    
    public float getSpentInMonth(int month){
        
        float spent = 0;
        Connection con = Connect.ConnectDB();
        String username = jLabelUsername.getText();

        try{
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT price FROM item WHERE username LIKE '"+username+"' AND month LIKE '"+month+"'");
            while(rs.next()){
                spent = spent + Float.valueOf(String.valueOf(rs.getObject(1)));
            }
        } catch(SQLException e){
            System.out.print(e);
        }
        return spent;
    }
    
